function findNearestPanorama(lat, lng) {
  const toRad = (deg) => (deg * Math.PI) / 180;
  const R = 6371000; // Earth radius in meters

  let nearest = null;
  let minDist = Infinity;

  for (const p of PANORAMAS) {
    const dLat = toRad(p.lat - lat);
    const dLng = toRad(p.lng - lng);
    const a =
      Math.sin(dLat / 2) * Math.sin(dLat / 2) +
      Math.cos(toRad(lat)) * Math.cos(toRad(p.lat)) *
      Math.sin(dLng / 2) * Math.sin(dLng / 2);
    const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
    const distance = R * c;

    if (distance < minDist) {
      minDist = distance;
      nearest = { id: p.id, lat: p.lat, lng: p.lng, heading: p.heading, distance: distance };
    }
  }

  return nearest;
}

function getPanoramaGeoJSON() {
  return {
    type: 'FeatureCollection',
    features: PANORAMAS.map((p) => ({
      type: 'Feature',
      geometry: {
        type: 'Point',
        coordinates: [p.lng, p.lat]
      },
      properties: {
        id: p.id,
        heading: p.heading
      }
    }))
  };
}

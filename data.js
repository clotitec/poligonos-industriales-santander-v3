// REASIGNACIÓN AUTOMÁTICA DE EMPRESAS POR PUNTO-EN-POLÍGONO
// Ray-casting algorithm para mover empresas a áreas correctas
// ============================================================
function _pointInPolygon(lat, lng, polygon) {
    let inside = false;
    for (let i = 0, j = polygon.length - 1; i < polygon.length; j = i++) {
        const xi = polygon[i][1], yi = polygon[i][0];
        const xj = polygon[j][1], yj = polygon[j][0];
        const intersect = ((yi > lng) !== (yj > lng)) &&
            (lat < (xj - xi) * (lng - yi) / (yj - yi) + xi);
        if (intersect) inside = !inside;
    }
    return inside;
}

// Nombres completos para las áreas nuevas
const _areaNombres = {
    'parayas-sur': 'Parayas Sur',
    'parayas-norte': 'Parayas Norte'
};

// Reasignar empresas que caigan en los polígonos nuevos
const _newPolygons = poligonos.filter(p => p.areaId === 'parayas-sur' || p.areaId === 'parayas-norte');
empresas.forEach(emp => {
    for (const poly of _newPolygons) {
        if (_pointInPolygon(emp.lat, emp.lng, poly.coordinates)) {
            emp.areaId = poly.areaId;
            emp.area = _areaNombres[poly.areaId];
            break;
        }
    }
});

